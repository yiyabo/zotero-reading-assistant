/**
 * Return an inline SVG string for the named icon.
 *
 * @param name  Icon identifier
 * @param size  Size in px applied to width/height (default: uses CSS --ra-icon-md)
 */
export function icon(name: IconName, size?: number): string {
  const paths = PATHS[name];
  if (!paths) return "";

  const dim = size != null ? `width="${size}" height="${size}"` : `style="width:var(--ra-icon-md,16px);height:var(--ra-icon-md,16px)"`;
  return `<svg xmlns="http://www.w3.org/2000/svg" ${dim} viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" aria-hidden="true">${paths}</svg>`;
}

